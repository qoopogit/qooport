    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed

        try {

            cliente.getConfig().setRutaArchivo(getTxtRutaArchivo().getText());
            cliente.getConfig().setVersion(getTxtVersion().getText());
            cliente.getConfig().setActualizarserver(getChkActualizarserver().isSelected());
            cliente.getConfig().setPedirArchivoOffline(getChkPedirArchivoOffline().isSelected());
            cliente.getConfig().agregarParametro("iniciarArranque", getIniciarArranque().isSelected());
            cliente.getConfig().agregarParametro("clave", Encriptacion.cifra(getTxtPassword().getText()));
            cliente.getConfig().agregarParametro("ipsbloqueadas", getTxtIps().getText());
            cliente.getConfig().agregarParametro("notificacion", getChkNotificar().isSelected());
            cliente.getConfig().agregarParametro("dirEquipos", txtRutaEquipos.getText());
            
            QoopoRT.MOSTRAR_NOTIFICACION = getChkNotificar().isSelected();

            SerializarUtil.escribirArchivo(cliente.getConfig(), "config.dat");
        } catch (Exception ex) {
        }

    }//GEN-LAST:event_btnGuardarActionPerformed

