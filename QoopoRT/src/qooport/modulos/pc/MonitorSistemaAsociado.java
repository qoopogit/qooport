    public void agregarDatoCPU(double sistema, double cpu) {
        graficoCPU.agregarDato(0, sistema);//system load
        graficoCPU.agregarDato(1, cpu);//cpu load
        graficoCPU.repaint();
        jLabel3.setText("Proceso Java=" + Util.redondear(sistema, 2) + "%  CPU=" + Util.redondear(cpu, 2) + "%");
    }

    public void agregarDatoRAM(double libre, double ram) {
        double ocupado = ram - libre;//tamanio ram - ram libre
        //cambia el valor maximo al maximo de ram del asociado
        graficoRAM.setValorMayor(ram);

        graficoRAM.agregarDato(ocupado);
        graficoRAM.repaint();
        jLabel4.setText("RAM=" + Util.convertirBytes(ram) + " Libre=" + Util.convertirBytes(libre) + " Ocupado=" + Util.convertirBytes(ocupado));
    }

    private void configurarMonitorCPU() {
        graficoCPU = new PanelMonitorCarga(300, 50);
        graficoCPU.setOpaque(true); //content panes must be opaque
        graficoCPU.iniciarSeries(2);
        graficoCPU.setColorSerie(0, Color.LIGHT_GRAY);
        graficoCPU.setColorSerie(1, Color.GREEN);
        graficoCPU.setIntervalo(1);
        graficoCPU.setDibujarespectrograma(true);
        graficoCPU.setValorMayor(100.0);//valor maximo de la serie
        graficoCPU.setMaximoTamanio(50);//maximo numero de muestras

        Accion accionCpu = new Accion() {
            @Override
            public void ejecutar(Object... parametros) {
                asociado.pedirCPUINFO();
            }
        };

        cargaCPU = new HiloAccion(accionCpu, 1000);
        tabPanCPU.addTab("CPU", graficoCPU);
    }

    private void configurarMonitorRAM() {
        graficoRAM = new PanelMonitorCarga(300, 50);
        graficoRAM.setOpaque(true); //content panes must be opaque
        graficoRAM.iniciarSeries(1);
        graficoRAM.setColorSerie(0, Color.CYAN);
        graficoRAM.setIntervalo(1);
        graficoRAM.setDibujarespectrograma(true);
        //maximo es el tamanio de la ram
        graficoRAM.setValorMayor(((com.sun.management.OperatingSystemMXBean) ManagementFactory.getOperatingSystemMXBean()).getTotalPhysicalMemorySize());
        graficoRAM.setMaximoTamanio(50);//maximo numero de muestras

        Accion accionRam = new Accion() {
            @Override
            public void ejecutar(Object... parametros) {
                asociado.pedirRAMINFO();
            }
        };
        cargaRAM = new HiloAccion(accionRam, 1000);

        tabPanRAM.addTab("RAM", graficoRAM);
//        jPanel1.add(graficoRAM);
    }

