    private void btnIniciarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIniciarActionPerformed
        if (btnIniciar.isSelected()) {
            btnIniciar.setIcon(Util.cargarIcono16("/resources/stop_close.png"));
            btnIniciar.setText("Detener");
            Accion accionRam = new Accion() {
                @Override
                public void ejecutar(Object... parametros) {
                    asociado.pedirRAMINFO();
                }
            };
            cargaRAM = new HiloAccion(accionRam, 1000);
            Accion accionCpu = new Accion() {
                @Override
                public void ejecutar(Object... parametros) {
                    asociado.pedirCPUINFO();
                }
            };

            cargaCPU = new HiloAccion(accionCpu, 1000);
            cargaCPU.iniciar();
            cargaRAM.iniciar();
        } else {
            btnIniciar.setIcon(Util.cargarIcono16("/resources/start.png"));
            btnIniciar.setText("Iniciar");
            cargaCPU.detener();
            cargaRAM.detener();

            cargaRAM = null;
            cargaCPU = null;
        }
    }//GEN-LAST:event_btnIniciarActionPerformed

